// Summation of primes

// The sum of the primes below 10 is 2 + 3 + 5 + 7 = 17.
// Find the sum of all the primes below two million.

//------------------------------------------FUNCTIONS------------------------------------------

const isPrime = num => {
  let check = true;
  for (let i = 2; i <= num / 2 && check; i++) {
    if (!(num % i)) {
      check = false;
    }
  }
  return check;
};

//------------------------------------------INPUT------------------------------------------

let limit = 2000000;

//------------------------------------------CORE CODE------------------------------------------

let sum = 0;
for (let i = limit; i > 1; i--) {
  if (isPrime(i)) {
    console.log(i);
    sum += i;
  }
}

//------------------------------------------OUPUT------------------------------------------

console.log(sum);

